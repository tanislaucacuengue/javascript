console.log("Digite um valor de 1 à 7")
var semana = 3
switch (semana) {
    case semana == 1:
        console.log("Domingo")
        break;
    case semana == 2:
        console.log("2ª Feira")
        break;
    case semana == 3:
        console.log("3ª Feira")
        break;
    case semana == 4:
        console.log("4ª Feira")
        break;
    case semana == 5:
        console.log("5ª Feira")
        break;
    case semana == 6:
        console.log("6ª Feira")
        break;
    case semana == 7:
        console.log("Sabado")
        break;


    default:
        console.log("O valor não é valido")
        break;
}